    private void ButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarActionPerformed
        Levantamentos levantamentos = new Levantamentos();
        GeralDAO<Levantamentos> gd = new GeralDAO<>();
        levantamentos.setCodParqueamento(Integer.parseInt(cbCodParqueamento.getSelectedItem().toString()));
        levantamentos.setNrBI(cbBI.getSelectedItem().toString());
        levantamentos.setValor(Integer.parseInt(TextFieldValor.getText()));
        levantamentos.setCodUtilizador(1);

        SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
        Date hoje = new Date();
        levantamentos.setDataL((df.format(hoje)));

        SimpleDateFormat dfa = new SimpleDateFormat("HH:mm:ss");
        Date agora = new Date();
        levantamentos.setHoraL(dfa.format(agora));

        gd.salvar(levantamentos);
        DefaultTableModel model = (DefaultTableModel) TableLevantamentos.getModel();
        model.addRow(new Object[]{"", "", cbBI.getSelectedItem().toString(), TextFieldValor.getText(),
            df.format(hoje), dfa.format(agora)});

    }//GEN-LAST:event_ButtonSalvarActionPerformed

