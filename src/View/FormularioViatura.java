    private void ButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarActionPerformed
       
        Viatura vi = new Viatura();
        
        GeralDAO<Viatura> gd = new GeralDAO<>();
        
        vi.setCodMotor(TextFieldCodMotor.getText());
        vi.setCodChassi(TextFieldNumChassis.getText());
        vi.setModelo(textFieldModelo.getText());
        vi.setMatricula(textFieldMatricula.getText());
        vi.setTipo(textFiledTipo.getText());
        vi.setFabricante(TextFieldFabricante.getText());
        vi.setStatus(true);
        vi.setEstado("optimo");
  
        
        gd.salvar(vi);
        
              
    }//GEN-LAST:event_ButtonSalvarActionPerformed


